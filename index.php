window.addEventListener("load", function () {
  setTimeout(function () {
    const popup = document.getElementById("video-popup");
    const closeBtn = document.querySelector(".video-popup-close");
    const video = document.getElementById("homePopupVideo");

    if (!popup || !video || !closeBtn) return;

    // Show popup and disable scroll
    popup.style.display = "flex";
    document.body.classList.add("no-scroll");
    document.documentElement.classList.add("no-scroll");

    // Define unmute function so we can remove it later
    function unmuteVideoOnce() {
      if (video.readyState >= 2) {
        video.pause();
        video.muted = false;
        video.currentTime = 0;
        video.play().catch(e => console.warn('Play failed:', e));
        document.removeEventListener("click", unmuteVideoOnce);
      } else {
        video.addEventListener("loadeddata", () => {
          video.pause();
          video.muted = false;
          video.currentTime = 0;
          video.play().catch(e => console.warn('Play failed:', e));
          document.removeEventListener("click", unmuteVideoOnce);
        }, { once: true });
      }
    }

    // Add the listener for first interaction
    document.addEventListener("click", unmuteVideoOnce);

    // Close popup and fully clean up video and listener
    closeBtn.addEventListener("click", function () {
      // Stop video
      video.pause();
      video.currentTime = 0;
      video.muted = true;

      // Remove the unmute listener so video can't restart on click
      document.removeEventListener("click", unmuteVideoOnce);

      // Remove video and popup
      video.remove();
      popup.remove();

      // Enable scroll
      document.body.classList.remove("no-scroll");
      document.documentElement.classList.remove("no-scroll");
    });

  }, 300); // Small delay to ensure everything is visually loaded
});
</script>





